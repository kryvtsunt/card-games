/**
 * Represents a suit of the Card.
 *
 * @author Tymofii Kryvtsun
 */
public interface Suit {
    /**
     * Gets Suit's Name.
     *
     * @return the Name of the Suit.
     */
    String getName();

    /**
     * Gets Symbol that represents the Suit.
     *
     * @return the Representational Symbol.
     */
    char getSymbol();
}
